package com.ba.pojo;

public class Ownlist {
	
	private String ownlist_id;
	private String book_name;
	private String author;
	private String availtype;
	private int quantity;
	public String getOwnlist_id() {
		return ownlist_id;
	}
	public void setOwnlist_id(String ownlist_id) {
		this.ownlist_id = ownlist_id;
	}
	public String getBook_name() {
		return book_name;
	}
	public void setBook_name(String book_name) {
		this.book_name = book_name;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public String getAvailtype() {
		return availtype;
	}
	public void setAvailtype(String availtype) {
		this.availtype = availtype;
	}
	public int getQuantity() {
		return quantity;
	}
	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}
	
	

}
